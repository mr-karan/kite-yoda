function replace() {
    window.setTimeout(function() {
        this.holdings = document.getElementsByClassName("holdings");
        if (holdings.length === 0) {
            replace();
        } else {
            this.holdings[1].innerText = getQuote()
        }
    }, 50)
}

// In case `/holdings` is directly loaded.
let lastUrl = location.href;
if (location.href.indexOf("holdings") > -1){
    replace()
}

// https://stackoverflow.com/a/39508954/709452
new MutationObserver(() => {
  const url = location.href;
  if (url !== lastUrl) {
    lastUrl = url;
    onUrlChange();
  }
}).observe(document, {subtree: true, childList: true});

// In case `/holdings` is reached via different pages.
function onUrlChange() {
    if (location.href.indexOf("holdings") > -1){
        replace()
    }
}

